// Reconcile is the main control loop.
func (r *RANDeploymentReconciler) Reconcile(ctx context.Context, req ctrl.Request) (ctrl.Result, error) {
	logger := log.FromContext(ctx).WithValues("NFDeployment", req.NamespacedName)
	logger.Info("Reconcile called")

	// 1. Fetch the NFDeployment.
	instance := &workloadv1alpha1.NFDeployment{}
	if err := r.Get(ctx, req.NamespacedName, instance); err != nil {
		if errors.IsNotFound(err) {
			logger.Info("NFDeployment not found; object was deleted")
			return ctrl.Result{}, nil
		}
		logger.Error(err, "Failed to get NFDeployment")
		return ctrl.Result{}, err
	}

	// 2. Filter by provider.
	if !slices.Contains(GetSupportedProviders(), instance.Spec.Provider) {
		logger.Info("Ignoring NFDeployment with unsupported provider", "provider", instance.Spec.Provider)
		return ctrl.Result{}, nil
	}
	logger.Info("Processing NFDeployment", "provider", instance.Spec.Provider)

	// 3. Read configuration from parametersRefs chain.
	configInfo, err := r.GetConfigs(ctx, instance)
	if err != nil {
		logger.Error(err, "Failed to get required ConfigInfo; requeueing")
		_ = r.updateStatusIfRequired(ctx, instance, metav1.Condition{
			Type:               "invalidConfigInfo",
			LastTransitionTime: metav1.Time{Time: time.Now()},
			Status:             metav1.ConditionFalse,
			Reason:             "invalidConfigInfo",
			Message:            fmt.Sprintf("Failed to get ConfigInfo: %v", err),
		})
		return ctrl.Result{RequeueAfter: 15 * time.Second}, err
	}

	// 4. Check if all required interfaces are present (IPs injected by nfdeploy-fn).
	// If not yet present, requeue without error (IPAM pipeline still running).
	for _, ifName := range []string{"n2", "n3", "e1", "f1c", "f1u"} {
		if _, ipErr := GetFirstInterfaceConfigIPv4(instance.Spec.Interfaces, ifName); ipErr != nil {
			logger.Info("Interface IP not yet available; requeueing", "interface", ifName)
			return ctrl.Result{RequeueAfter: 10 * time.Second}, nil
		}
	}

	const finalizerName = "srsran.nephio.org/finalizer"
	gnbResource := GnbResources{}

	if instance.DeletionTimestamp.IsZero() {
		// Object is not being deleted – always reconcile resources.
		errList := r.CreateAll(ctx, instance, gnbResource, configInfo)

		var cond metav1.Condition
		if len(errList) == 0 {
			cond = metav1.Condition{
				Type:               "resourceCreation",
				LastTransitionTime: metav1.Time{Time: time.Now()},
				Status:             metav1.ConditionTrue,
				Reason:             "resourceCreation",
				Message:            "All srsRAN gNB resources reconciled successfully",
			}
		} else {
			msgs := ""
			for _, e := range errList {
				msgs += e.Error() + "; "
			}
			cond = metav1.Condition{
				Type:               "resourceCreation",
				LastTransitionTime: metav1.Time{Time: time.Now()},
				Status:             metav1.ConditionFalse,
				Reason:             "resourceCreation",
				Message:            msgs,
			}
		}
		if err := r.updateStatusIfRequired(ctx, instance, cond); err != nil {
			logger.Error(err, "Failed to update status after resource reconciliation")
		}

		// Set Ready condition for ConfigSync compatibility
		var readyCond metav1.Condition
		if len(errList) == 0 {
			readyCond = metav1.Condition{
				Type:               "Ready",
				LastTransitionTime: metav1.Time{Time: time.Now()},
				Status:             metav1.ConditionTrue,
				Reason:             "Reconciled",
				Message:            "NFDeployment reconciled successfully",
			}
		} else {
			readyCond = metav1.Condition{
				Type:               "Ready",
				LastTransitionTime: metav1.Time{Time: time.Now()},
				Status:             metav1.ConditionFalse,
				Reason:             "ReconcileFailed",
				Message:            "NFDeployment reconciliation failed",
			}
		}
		if err := r.updateStatusIfRequired(ctx, instance, readyCond); err != nil {
			logger.Error(err, "Failed to update Ready status")
		}

		// Ensure finalizer is present so cleanup runs on deletion.
		if !controllerutil.ContainsFinalizer(instance, finalizerName) {
			controllerutil.AddFinalizer(instance, finalizerName)
			if err := r.Update(ctx, instance); err != nil {
				return ctrl.Result{}, err
			}
		}
	} else {
		// Object is being deleted.
		if controllerutil.ContainsFinalizer(instance, finalizerName) {
			logger.Info("Deleting srsRAN gNB resources")
			errList := r.DeleteAll(ctx, instance, gnbResource, configInfo)

			var cond metav1.Condition
			if len(errList) == 0 {
				cond = metav1.Condition{
					Type:               "resourceDeletion",
					LastTransitionTime: metav1.Time{Time: time.Now()},
					Status:             metav1.ConditionTrue,
					Reason:             "resourceDeletion",
					Message:            "All srsRAN gNB resources deleted successfully",
				}
			} else {
				msgs := ""
				for _, e := range errList {
					msgs += e.Error() + "; "
				}
				cond = metav1.Condition{
					Type:               "resourceDeletion",
					LastTransitionTime: metav1.Time{Time: time.Now()},
					Status:             metav1.ConditionFalse,
					Reason:             "resourceDeletion",
					Message:            msgs,
				}
			}
			if err := r.updateStatusIfRequired(ctx, instance, cond); err != nil {
				logger.Error(err, "Failed to update status after resource deletion")
			}

			controllerutil.RemoveFinalizer(instance, finalizerName)
			if err := r.Update(ctx, instance); err != nil {
				return ctrl.Result{}, err
			}
		}
		return ctrl.Result{}, nil
	}

	return ctrl.Result{}, nil
}

// findNFDeploymentsForConfig returns reconcile requests for all NFDeployments
// that reference the changed config CR via NFConfig.
func (r *RANDeploymentReconciler) findNFDeploymentsForConfig(ctx context.Context, obj client.Object) []reconcile.Request {
	// Determine the Kind from the concrete type, since obj.GetObjectKind() often
	// returns empty GVK for objects read from the cache in controller-runtime.
	var objKind string
	switch obj.(type) {
	case *srsranov1alpha1.SrsRANCellConfig:
		objKind = "SrsRANCellConfig"
	case *srsranov1alpha1.PLMNConfig:
		objKind = "PLMNConfig"
	case *srsranov1alpha1.SrsRANConfig:
		objKind = "SrsRANConfig"
	default:
		objKind = obj.GetObjectKind().GroupVersionKind().Kind
	}

	logger := log.FromContext(ctx).WithValues("configKind", objKind, "configName", obj.GetName())
	logger.Info("Config change detected, looking for affected NFDeployments")

	// List all NFDeployments in the same namespace
	nfDeployList := &workloadv1alpha1.NFDeploymentList{}
	if err := r.List(ctx, nfDeployList, client.InNamespace(obj.GetNamespace())); err != nil {
		logger.Error(err, "Failed to list NFDeployments")
		return nil
	}

	var requests []reconcile.Request
	for _, nfDeploy := range nfDeployList.Items {
		// Only process NFDeployments with supported providers
		if !slices.Contains(GetSupportedProviders(), nfDeploy.Spec.Provider) {
			continue
		}

		// Check if this NFDeployment references an NFConfig that references the changed config
		for _, ref := range nfDeploy.Spec.ParametersRefs {
			if ref.APIVersion == "workload.nephio.org/v1alpha1" && ref.Kind == "NFConfig" && ref.Name != nil {
				nfConfig := &workloadv1alpha1.NFConfig{}
				if err := r.Get(ctx, types.NamespacedName{Name: *ref.Name, Namespace: nfDeploy.Namespace}, nfConfig); err != nil {
					continue
				}
				// Check if this NFConfig references the changed config CR
				for _, configRef := range nfConfig.Spec.ConfigRefs {
					kind, _ := extractKindFromRaw(runtime.RawExtension{Raw: configRef.Raw})
					name, _ := extractNameFromRaw(runtime.RawExtension{Raw: configRef.Raw})
					if kind == objKind && name == obj.GetName() {
						logger.Info("Enqueueing NFDeployment for config change", "nfDeployment", nfDeploy.Name)
						requests = append(requests, reconcile.Request{
							NamespacedName: types.NamespacedName{
								Name:      nfDeploy.Name,
								Namespace: nfDeploy.Namespace,
							},
						})
						break
					}
				}
			}
		}
	}
	return requests
}

// SetupWithManager registers the controller with the Manager.
func (r *RANDeploymentReconciler) SetupWithManager(mgr ctrl.Manager) error {
	return ctrl.NewControllerManagedBy(mgr).
		For(&workloadv1alpha1.NFDeployment{}).
		Watches(&srsranov1alpha1.SrsRANCellConfig{},
			handler.EnqueueRequestsFromMapFunc(r.findNFDeploymentsForConfig)).
		Watches(&srsranov1alpha1.PLMNConfig{},
			handler.EnqueueRequestsFromMapFunc(r.findNFDeploymentsForConfig)).
		Watches(&srsranov1alpha1.SrsRANConfig{},
			handler.EnqueueRequestsFromMapFunc(r.findNFDeploymentsForConfig)).
		Complete(r)
}

