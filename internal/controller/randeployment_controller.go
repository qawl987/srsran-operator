// Package controller implements the srsRAN gNB NFDeployment reconciler.
//
// Architecture: three SEPARATE pods are deployed per NFDeployment (provider=gnb.srsran.io):
//
//	CU-CP  – binds N2 (NGAP→AMF), E1 (E1AP↔CU-UP), F1C (F1-AP↔DU)
//	CU-UP  – binds N3 (GTP-U→UPF), E1 (E1AP←CU-CP), F1U (GTP-U↔DU)
//	DU     – binds F1C (F1-AP←CU-CP), F1U (GTP-U←CU-UP), ZMQ RF
//
// IPs come exclusively from NFDeployment.spec.interfaces[] populated by the
// Nephio nfdeploy-fn (2nd pass) after the generic-specializer has resolved IPAM.
package controller

