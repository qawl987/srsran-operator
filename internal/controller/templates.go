// CU-CP configuration template
// ─────────────────────────────────────────────────────────────────────────────

// CUCPConfigValues holds the dynamic values injected into the CU-CP config.
type CUCPConfigValues struct {
	// N2BindAddr is the NGAP bind address on the N2 interface (CU-CP ↔ AMF).
	N2BindAddr string
	// AMFAddr is the AMF NGAP address (IP or hostname).
	AMFAddr string
	// E1BindAddr is the E1AP server bind address (CU-CP ↔ CU-UP).
	E1BindAddr string
	// F1CBindAddr is the F1-AP server bind address (CU-CP ↔ DU).
	F1CBindAddr string
	// PLMN is the 5-digit PLMN string (e.g. "20893").
	PLMN string
	// TAC is the Tracking Area Code.
	TAC uint32
	// Slices is the list of S-NSSAI slices advertised in supported_tracking_areas.
	Slices []srsranov1alpha1.SliceInfo
	// InactivityTimer in seconds (default 7200).
	InactivityTimer int
	// RequestPDUSessionTimeout in seconds (default 20).
	RequestPDUSessionTimeout int
}

