// ─────────────────────────────────────────────────────────────────────────────

// DUConfigValues holds the dynamic values injected into the DU config template.
type DUConfigValues struct {
	// F1CCUCPAddr is the CU-CP Service DNS for F1-AP client connection.
	F1CCUCPAddr string
	// F1CBindAddr is the DU F1-AP (control) bind address.
	F1CBindAddr string
	// F1UBindAddr is the DU F1-U (user plane) bind address.
	F1UBindAddr string
	// ZMQBindAddr is the address the DU ZMQ RF socket binds to.
	ZMQBindAddr string
	// ZMQTxPort is the ZMQ TX port.
	ZMQTxPort int
	// ZMQRxPort is the ZMQ RX port.
	ZMQRxPort int
	// SRate is the RF sample rate in MHz (e.g. "23.04").
	SRate string
	// TxGain is the RF transmit gain.
	TxGain uint32
	// RxGain is the RF receive gain.
	RxGain uint32
	// DlArfcn is the downlink ARFCN.
	DlArfcn uint32
	// Band is the NR operating band number.
	Band uint32
	// ChannelBandwidthMHz is the channel bandwidth in MHz.
	ChannelBandwidthMHz uint32
	// CommonScs is the subcarrier spacing in kHz.
	CommonScs uint32
	// PLMN is the 5-digit PLMN string.
	PLMN string
	// TAC is the Tracking Area Code.
	TAC uint32
	// PCI is the Physical Cell ID.
	PCI uint32
	// PDCCH holds the PDCCH configuration values.
	PDCCH srsranov1alpha1.SrsRANPDCCHConfig
	// PRACH holds the PRACH configuration values.
	PRACH srsranov1alpha1.SrsRANPRACHConfig
	// PDSCHMcsTable is the MCS table for PDSCH ("qam64" or "qam256").
	PDSCHMcsTable string
	// PUSCHMcsTable is the MCS table for PUSCH ("qam64" or "qam256").
	PUSCHMcsTable string
}

